function reverseArray(array) {
  return array.slice().reverse();
}

// scores for the individual pieces
const pieceScores = {
  king: 900,
  queen: 90,
  bishop: 30,
  knight: 30,
  rook: 50,
  pawn: 10
};

// scores for the positions of chess pieces
const positionScores = {
  king: [whiteKingHeuristic, blackKingHeuristic],
  queen: [whiteQueenHeuristic, blackQueenHeuristic],
  bishop: [whiteBishopHeuristic, blackBishopHeuristic],
  knight: [whiteKnightHeuristic, blackKnightHeuristic],
  rook: [whiteRookHeuristic, blackRookHeuristic],
  pawn: [whitePawnHeuristic, blackPawnHeuristic]
};

// returns total score of a piece at a certain position
function pieceVal(piece, userColor) {
  if (piece === null) return 0;
  const pieceScore = pieceScores[piece.name];
  const positionScore =
    positionScores[piece.name][piece.color][piece.row][piece.col];
  const score = pieceScore + positionScore;
  if (piece.color === userColor) return -score;
  else return score;
}

// evaluates the heuristic score of a given board
export default function evaluateHeuristic(board, userColor) {
  let heuristicScore = 0;
  for (let row = 0; row < numOfRows; row++) {
    for (let col = 0; col < numOfCols; col++) {
      heuristicScore += pieceVal(board.pieceAt(row, col), userColor);
    }
  }
  return heuristicScore;
}
